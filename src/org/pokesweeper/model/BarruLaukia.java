package org.pokesweeper.model;

public class BarruLaukia {
	
	private boolean mina;
	private boolean begiratuta;
	private int egoera;
	
	public BarruLaukia() {}

	public boolean isMina() {
		return mina;
	}

	public void setMina(boolean mina) {
		this.mina = mina;
	}

	public boolean isBegiratuta() {
		return begiratuta;
	}

	public void setBegiratuta(boolean begiratuta) {
		this.begiratuta = begiratuta;
	}

	public int getEgoera() {
		return egoera;
	}

	public void setEgoera(int egoera) {
		this.egoera = egoera;
	}
	
	

}
